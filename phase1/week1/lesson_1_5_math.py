# 任務 1：兩個數字的基本運算
a = 10 
b = 3
print(f"a + b = {a + b}")
print(f"a - b = {a - b}")
print(f"a * b = {a * b}")
print(f"a / b = {a / b}")
print(f"a // b = {a // b}")
print(f"a % b = {a % b}")
print(f"a ** b = {a ** b}")

# 任務 2：計算圓面積
radius = 5
area = 3.14 * radius ** 2
print(f"圓面積 = {area}")

# 任務 3：計算 BMI
height = 1.74
weight = 70
bmi = weight / (height ** 2)
print(f"BMI = {bmi:.2f}")

# 任務 4：攝氏轉華氏
celsius = 30
fahrenheit = (celsius * 9) / 5 + 32
print(f"攝氏溫度 30 度轉換成華氏溫度是 : {fahrenheit} 度")

# 任務 5：商品打折後價格
price = 1000
discount = 0.8
discounted_price = price * discount
print(f"商品打折後價格 = {discounted_price}")